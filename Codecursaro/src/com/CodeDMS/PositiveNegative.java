package com.CodeDMS;

public class PositiveNegative {
	
	public static void main(String[] args) {
		
		// to check if the number is Negative or Positive
		
		int number = -20;
		
		if (number >= 0) {
			
			System.out.println("The number is Positive");
		}
		else {
			
			System.out.println("The number is Negative");
		}
				
	}

}
